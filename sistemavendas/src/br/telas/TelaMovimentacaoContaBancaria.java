    private void preencheTabela(List<ItemContaBancaria> contas) {
        Collections.sort(contas);
        ItemContaBancariaTableModel crtm = new ItemContaBancariaTableModel(contas);
        tbContas.setModel(crtm);
        FormataTamanhoColunasJTable.packColumns(tbContas, 1);
        tbContas.setAutoCreateRowSorter(true);
        double valorDebito = 0, valorPago = 0;
        
        tbContas.setAutoCreateRowSorter(true);
        tbContas.setDefaultRenderer(Object.class,
                new ItemContaBancariaCellRenderer());
        
        double entrada = 0, saida = 0, bloqueado = 0;
        for (ItemContaBancaria icb : contas) {
            if (icb.isBloqueada()) {
                double valor = (icb.getEntrada() == 0 ? icb.getSaida() : icb.getEntrada());
                bloqueado += valor;
            } else {
                entrada += icb.getEntrada();
                saida += icb.getSaida();
            }
        }
        lblBloqueado.setText(Util.acertarNumero(bloqueado));
        lblEntrada.setText(Util.acertarNumero(entrada));
        lblSaida.setText(Util.acertarNumero(saida));
        lblSaldo.setText(Util.acertarNumero(entrada - saida));
        
    }

    private void btEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btEditActionPerformed
        int i = tbContas.getSelectedRow();
        if (i >= 0) {
            ItemContaBancariaTableModel itm = (ItemContaBancariaTableModel) tbContas.getModel();
            ItemContaBancaria icb = itm.getValueAt(i);
            TelaMoviCadEditaContaBancaria tela = new TelaMoviCadEditaContaBancaria(icb);
            tela.setVisible(true);
            btPesquisarActionPerformed(null);
        } else {
            JOptionPane.showMessageDialog(rootPane, "Selecione o Ítem!");
        }
    }//GEN-LAST:event_btEditActionPerformed

