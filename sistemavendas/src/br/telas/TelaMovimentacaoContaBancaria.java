    private void btRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btRemoverActionPerformed
        int i = tbContas.getSelectedRow();
        if (i >= 0) {
            ItemContaBancariaTableModel itm = (ItemContaBancariaTableModel) tbContas.getModel();
            ItemContaBancaria icb = itm.getValueAt(i);
            if (JOptionPane.showConfirmDialog(rootPane, "Deseja Excluir o Item?",
                    "EXCLUIR", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_NO_OPTION) {
                ItemContaBancariaDAO ibDAO = new ItemContaBancariaDAO();
                ibDAO.remove(icb);
                
                btPesquisarActionPerformed(null);
            }
            
        } else {
            JOptionPane.showMessageDialog(rootPane, "Selecione o Ítem!");
        }
    }//GEN-LAST:event_btRemoverActionPerformed

